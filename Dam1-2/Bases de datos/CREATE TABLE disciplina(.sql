CREATE TABLE disciplina(
id_nombre varchar(20) NOT NULL,
num_jugadores int default 0,
id_torneo int NOT NULL
);

CREATE TABLE torneo(
nombre varchar(20),
id_torneo int NOT NULL,
PRIMARY KEY(id_torneo),
fecha_inicio date default "2021-01-01" not null,
duracion int
);

create table juega(
nombre_local varchar(20),
nombre_visitante varchar(20),
juega_partido int,
faltas int zerofill,
goles int default 0,
fueras_de_juego int default 0,
penaltis int default 0
);

CREATE TABLE estadio(
nombre varchar(20) not null,
PRIMARY KEY(nombre),
fecha_construccion date,
aforo int NOT NULL,
id_equipo int
);

CREATE TABLE equipo(
nombre varchar(20) unique,
fecha_creacion date NOT NULL,
id_equipo int NOT NULL,
PRIMARY KEY (id_equipo)
);

CREATE TABLE aficionado(
dni varchar(9),
edad int,
numero_aficionado int unique key,
PRIMARY KEY (dni, numero_aficionado)
);

CREATE TABLE jugador(
nombre varchar(20),
edad int,
dni varchar(20),
id_equipo int,
PRIMARY KEY (dni),
FOREIGN KEY(id_equipo) REFERENCES equipo(id_equipo)
);

CREATE TABLE entrenador(
dni varchar(9),
PRIMARY KEY (dni),
edad int,
nombre varchar(20),
numero_entrenador int,
id_equipo int not null
);

CREATE TABLE premios(
cod_premio int,
descripcion varchar(30),
PRIMARY KEY (cod_premio)
);

CREATE TABLE gana_torneo(
id_torneo int,
id_equipo int,
cod_premio int,
fvictoria date,
FOREIGN KEY(id_torneo) REFERENCES torneo(id_torneo),
FOREIGN KEY(cod_premio) REFERENCES premios(cod_premio),
FOREIGN KEY(id_equipo) REFERENCES equipo(id_equipo)
);


